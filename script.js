let currentRegion = 'all'; 

function init() {
    checkNightMode(); 
    render();
    
    document.getElementById('regionSelect').onchange = (e) => { currentRegion = e.target.value; render(); };
    document.getElementById('openStatsBtn').onclick = calculateStats;
    document.getElementById('prevMonth').onclick = () => { viewDate.setMonth(viewDate.getMonth() - 1); render(); };
    document.getElementById('nextMonth').onclick = () => { viewDate.setMonth(viewDate.getMonth() + 1); render(); };
    document.getElementById('closeModalBtn').onclick = () => document.getElementById('modalOverlay').classList.remove('active');
    document.getElementById('closeStatsBtn').onclick = () => document.getElementById('statsOverlay').classList.remove('active');
}

function render() {
    const grid = document.getElementById('calendarGrid');
    const monthYearText = document.getElementById('monthDisplay');
    grid.innerHTML = '';

    const year = viewDate.getFullYear();
    const month = viewDate.getMonth();
    const monthsNames = ["Январь", "Февраль", "Март", "Апрель", "Май", "Июнь", "Июль", "Август", "Сентябрь", "Октябрь", "Ноябрь", "Декабрь"];
    monthYearText.innerText = `${monthsNames[month]} ${year}`;

    const firstDay = new Date(year, month, 1).getDay();
    const shift = firstDay === 0 ? 6 : firstDay - 1;
    const daysCount = new Date(year, month + 1, 0).getDate();

    for(let i=0; i<shift; i++) grid.appendChild(document.createElement('div'));

    for(let d=1; d<=daysCount; d++) {
        const dayEl = document.createElement('div');
        dayEl.className = 'day';
        dayEl.innerText = d;

        const dateKey = `${String(month + 1).padStart(2, '0')}-${String(d).padStart(2, '0')}`;
        const storageKey = `${year}-${dateKey}`;
        const holiday = holidayDB[dateKey];

        const dayOfWeek = new Date(year, month, d).getDay();
        if (dayOfWeek === 0 || dayOfWeek === 6) {
            dayEl.classList.add('weekend');
        }

        if(holiday && (currentRegion === 'all' || holiday.reg === currentRegion)) dayEl.classList.add('holiday');
        if(userMoods[storageKey]) dayEl.classList.add(`m-${userMoods[storageKey]}`);

        dayEl.onclick = () => selectDay(d, dateKey, storageKey, dayEl, holiday);
        grid.appendChild(dayEl);
    }
}

function selectDay(day, hKey, sKey, el, holiday) {
    document.querySelectorAll('.day').forEach(d => d.classList.remove('selected'));
    el.classList.add('selected');
    currentSelection = { dateKey: hKey, storageKey: sKey };

    const monthsGen = ["января", "февраля", "марта", "апреля", "мая", "июня", "июля", "августа", "сентября", "октября", "ноября", "декабря"];
    document.getElementById('selectedDateName').innerText = `${day} ${monthsGen[viewDate.getMonth()]}`;
    
    const isHolidayVisible = holiday && (currentRegion === 'all' || holiday.reg === currentRegion);
    
    let desc = isHolidayVisible ? holiday.d : "Обычный день. Самое время создать свой праздник!";
    if (userMoods[sKey]) desc += `\nНастроение: ${getMoodName(userMoods[sKey])}`;
    if (userNotes[sKey]) desc += `\n📝 Заметка: ${userNotes[sKey]}`;
    document.getElementById('selectedHolidayDesc').innerText = desc;

    const detailBtn = document.getElementById('openDetails');
    detailBtn.style.display = isHolidayVisible ? 'inline-block' : 'none';

    detailBtn.onclick = () => openModal('info', holiday);
    document.getElementById('editNote').onclick = () => openModal('note', holiday);
}

window.switchTab = function(tabName) {
    ['btnTabHow', 'btnTabHistory', 'btnTabChecklist'].forEach(id => {
        document.getElementById(id).classList.remove('active');
    });
    ['tabHow', 'tabHistory', 'tabChecklist'].forEach(id => {
        document.getElementById(id).classList.remove('active');
    });

    if (tabName === 'how') {
        document.getElementById('btnTabHow').classList.add('active');
        document.getElementById('tabHow').classList.add('active');
    } else if (tabName === 'history') {
        document.getElementById('btnTabHistory').classList.add('active');
        document.getElementById('tabHistory').classList.add('active');
    } else {
        document.getElementById('btnTabChecklist').classList.add('active');
        document.getElementById('tabChecklist').classList.add('active');
    }
};

function openModal(mode, holiday) {
    const { storageKey } = currentSelection;
    
    const modalTabs = document.getElementById('modalTabs');
    const infoContents = document.getElementById('infoContents');
    const noteContents = document.getElementById('noteContents');
    
    const title = document.getElementById('modalTitle');
    const mainText = document.getElementById('modalMainText');

    if (mode === 'info') {
        title.innerText = holiday.n;
        mainText.innerText = holiday.d;
        
        modalTabs.style.display = 'flex';
        infoContents.style.display = 'block';
        noteContents.style.display = 'none';
        
        document.getElementById('howText').innerText = holiday.how || "Информация о праздновании уточняется.";
        document.getElementById('historyText').innerText = holiday.hist || "История пока не добавлена.";

        const checkContainer = document.getElementById('checklistContainer');
        checkContainer.innerHTML = '';
        if (holiday.tasks && holiday.tasks.length > 0) {
            if (!userTasks[storageKey]) userTasks[storageKey] = []; 
            
            holiday.tasks.forEach((task, index) => {
                const label = document.createElement('label');
                label.className = 'task-item';
                
                const checkbox = document.createElement('input');
                checkbox.type = 'checkbox';
                checkbox.checked = userTasks[storageKey][index] === true;
                
                checkbox.onchange = (e) => {
                    userTasks[storageKey][index] = e.target.checked;
                    localStorage.setItem('myTasks', JSON.stringify(userTasks));
                };

                const span = document.createElement('span');
                span.innerText = task;
                label.appendChild(checkbox); label.appendChild(span); checkContainer.appendChild(label);
            });
        }
        
        switchTab('how');

    } else if (mode === 'note') {
        title.innerText = "Мой день";
        mainText.innerText = holiday ? `Праздник сегодня: ${holiday.n}` : "Как прошел ваш день?";
        
        modalTabs.style.display = 'none';
        infoContents.style.display = 'none';
        noteContents.style.display = 'block';
        
        document.getElementById('userNoteInput').value = userNotes[storageKey] || "";
        document.querySelectorAll('.mood-dot').forEach(dot => {
            dot.classList.remove('selected-mood');
            if(dot.dataset.mood === userMoods[storageKey]) dot.classList.add('selected-mood');
            dot.onclick = () => {
                document.querySelectorAll('.mood-dot').forEach(d => d.classList.remove('selected-mood'));
                dot.classList.add('selected-mood');
            };
        });
    }

    document.getElementById('modalOverlay').classList.add('active');
}

document.getElementById('saveNoteBtn').onclick = () => {
    const { storageKey } = currentSelection;
    const noteVal = document.getElementById('userNoteInput').value;
    const activeMood = document.querySelector('.mood-dot.selected-mood')?.dataset.mood;

    if(noteVal.trim()) userNotes[storageKey] = noteVal; else delete userNotes[storageKey];
    if(activeMood) userMoods[storageKey] = activeMood; else delete userMoods[storageKey];

    localStorage.setItem('myNotes', JSON.stringify(userNotes));
    localStorage.setItem('myMoods', JSON.stringify(userMoods));
    
    document.getElementById('modalOverlay').classList.remove('active');
    render(); 
};

function calculateStats() {
    const now = new Date();
    const nextNY = new Date(now.getFullYear() + 1, 0, 1);
    document.getElementById('daysToNY').innerText = Math.ceil((nextNY - now) / 86400000);
    document.getElementById('totalNotes').innerText = Object.keys(userNotes).length;

    let moodCounts = { 'happy': 0, 'calm': 0, 'prod': 0, 'sad': 0, 'party': 0 };
    for (let key in userMoods) if (moodCounts[userMoods[key]] !== undefined) moodCounts[userMoods[key]]++;

    const container = document.getElementById('moodStatsContainer');
    container.innerHTML = '';
    const displayMap = [{ key: 'happy', icon: '😄 Счастье' }, { key: 'prod', icon: '🚀 Продуктивность' }, { key: 'calm', icon: '😌 Спокойствие' }, { key: 'party', icon: '🥳 Вечеринки' }, { key: 'sad', icon: '😢 Грусть' }];

    displayMap.forEach(item => {
        if (moodCounts[item.key] > 0) {
            const row = document.createElement('div');
            row.className = 'mood-stat-row';
            row.innerHTML = `<span>${item.icon}</span> <span style="color:var(--primary)">${moodCounts[item.key]} дн.</span>`;
            container.appendChild(row);
        }
    });

    if(container.innerHTML === '') container.innerHTML = '<p style="color:#94a3b8; font-size: 14px; text-align: center;">Вы еще не отмечали настроение. Попробуйте сегодня!</p>';
    document.getElementById('statsOverlay').classList.add('active');
}

